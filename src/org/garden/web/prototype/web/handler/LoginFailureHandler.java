package org.garden.web.prototype.web.handler;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.garden.web.prototype.web.Constants;
import org.springframework.security.authentication.AuthenticationServiceException;
import org.springframework.security.authentication.BadCredentialsException;
import org.springframework.security.authentication.DisabledException;
import org.springframework.security.core.AuthenticationException;
import org.springframework.security.web.authentication.SimpleUrlAuthenticationFailureHandler;

/**
 * LoginFailureHandler.java
 *
 * @author Garden
 * create on 2014年11月10日 下午5:11:17
 */
public class LoginFailureHandler extends SimpleUrlAuthenticationFailureHandler {
	private static Log log = LogFactory.getLog(LoginFailureHandler.class);
	/* (non-Javadoc)
	 * @see org.springframework.security.web.authentication.SimpleUrlAuthenticationFailureHandler#onAuthenticationFailure(javax.servlet.http.HttpServletRequest, javax.servlet.http.HttpServletResponse, org.springframework.security.core.AuthenticationException)
	 */
	@Override
	public void onAuthenticationFailure(HttpServletRequest request,
			HttpServletResponse response, AuthenticationException exception)
			throws IOException, ServletException {
		if (exception.getClass().isAssignableFrom(
				AuthenticationServiceException.class)) {
			showMessage(request, "用户不存在");
		} else if (exception.getClass().isAssignableFrom(
				DisabledException.class)) {

		} else if (exception.getClass().isAssignableFrom(
				BadCredentialsException.class)) {
			showMessage(request, "密码错误");
		} else {
			showMessage(request, "未知登录错误");
		}
		log.warn("!!!登录失败!!!:", exception);
		super.onAuthenticationFailure(request, response, exception);
	}

	public void showMessage(HttpServletRequest request, String msg) {
		request.setAttribute( Constants.MESSAGE_KEY, msg);
	}
}
